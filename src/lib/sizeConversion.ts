export function euToCm(euSize: number | string, brandName?: string): string {
  const key = String(euSize)
  if (brandName) {
    const slug = brandName.toLowerCase().trim().replace(/\s+/g, '-')
    const brandMap = CM_BY_BRAND[slug]
    if (brandMap?.[key]) return brandMap[key]
  }
  return DEFAULT_CM[key] || ''
}
